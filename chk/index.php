<script title="ajax do checker">
    function start() {
        var e = document.getElementById("gate");
        var gate = e.options[e.selectedIndex].value;
        var linha = $("#lista").val();
        var linhastart = linha.split("\n");
        var total = linhastart.length;
        var ap = 0;
        var ed = 0;
        var ot = 0;
        var rp = 0;
        linhastart.forEach(function(value, index) {
            setTimeout(
                 function() {
					var sec = $("#sec").val();
                    $.ajax({
                        url: gate + '?lista=' + value + '&sec=' + sec,
                        type: 'GET',
                        async: true,
                        success: function(resultado) {
                            if (resultado.match("Approve CVV")){
                                removelinha();
                                ap++;
                                aprovadas(resultado + "");
                            }else if(resultado.match("Approve CVV")){
                              removelinha();
                            ap++;
                                aprovadas(resultado + "");
                             }else if(resultado.match("Approve CCN")){
                            	removelinha();
                            ed++;
                            edrovadas(resultado + "");
                             }else if(resultado.match("OTP")){
                            	removelinha();
                            ot++;
                              otpc(resultado + "");
                             }else {
                                removelinha();
                                rp++;
                                reprovadas(resultado + "");
                            }
                            $('#carregadas').html(total);
                            var fila = parseInt(ap) + parseInt(ed) + parseInt(ot) + parseInt(rp);
                            $('#cLive').html(ap);
                            $('#cWarn').html(ed);
                            $('#otp').html(ot);
                            $('#cDie').html(rp);
                            $('#total').html(fila);
                            $('#cLive2').html(ap);
                            $('#cWarn2').html(ed);
                            $('#otp2').html(ot);
                            $('#cDie2').html(rp);
                        }
                    });
                }, 6 * index);
        });
    }
    function aprovadas(str) {
        $(".aprovadas").append(str + "<br>");
    }
    function reprovadas(str) {
        $(".reprovadas").append(str + "<br>");
    }
    function otpc(str) {
        $(".otpc").append(str + "<br>");
    }
    function edrovadas(str) {
        $(".edrovadas").append(str + "<br>");
    }
    function removelinha() {
        var lines = $("#lista").val().split('\n');
        lines.splice(0, 1);
        $("#lista").val(lines.join("\n"));
    }
